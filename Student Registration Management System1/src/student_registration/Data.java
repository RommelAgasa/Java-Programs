    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        DefaultTableModel table = (DefaultTableModel)recordTable.getModel();
        String search = txtSearch.getText();
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(table);
        recordTable.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(search));
    }//GEN-LAST:event_txtSearchKeyReleased

    // Update ---------------------------------------------------------------------------------------
    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        DefaultTableModel data = (DefaultTableModel)recordTable.getModel();
        int selectedRow = recordTable.getSelectedRow();
        
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(this, "Select data to be deleted", "Student Registration Management System",
                    JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
        if(JOptionPane.showConfirmDialog(this, "Are you sure you want to delete the record of " 
                + recordTable.getValueAt(selectedRow, 1).toString(), "Student Registration Management System", 
                JOptionPane.YES_NO_OPTION) == JOptionPane.NO_OPTION){
            return;
        }
        
        try{
            int id = Integer.parseInt(data.getValueAt(selectedRow, 0).toString());
            Class.forName("com.mysql.cj.jdbc.Driver");
            sqlConnection = DriverManager.getConnection(databaseConnection, username, password);
            String queryDelete = "Delete from record where id = '"+id+"';";
            preparedStatement = sqlConnection.prepareStatement(queryDelete);
            preparedStatement.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Data has been deleted succesfully", "Student Registration Management System",
                    JOptionPane.INFORMATION_MESSAGE);
            
            loadData();
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Data.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Data.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_btnDeleteActionPerformed

    
        private int classSection(String sec){
        
        switch(sec){
            case "A": return 0;
            case "B": return 1;
            case "C": return 2;
            case "D": return 3;
        }
        
        return 0;
    }

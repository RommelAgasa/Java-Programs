    public void loadData() {
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            sqlConnection = DriverManager.getConnection(databaseConnection, username, password);
            String query = "Select * from record";
            preparedStatement = sqlConnection.prepareStatement(query);
            resultSet = preparedStatement.executeQuery();
            
            ResultSetMetaData stData = resultSet.getMetaData();
            
            int columnCount = stData.getColumnCount();
            
            DefaultTableModel table =   (DefaultTableModel)recordTable.getModel();
            table.setRowCount(0);
            
            
            while(resultSet.next()){
                Vector columnData = new Vector();
                
                for(int i = 1; i <= columnCount; i++){
                    columnData.add(resultSet.getString("id"));
                    columnData.add(resultSet.getString("name"));
                    columnData.add(resultSet.getString("fatherName"));
                    columnData.add(resultSet.getString("MotherName"));
                    columnData.add(resultSet.getString("dateOfBirth"));
                    columnData.add(resultSet.getString("gender"));
                    columnData.add(resultSet.getString("gradeLevel"));
                    columnData.add(resultSet.getString("class"));
                    columnData.add(resultSet.getString("admissionDate"));
                    columnData.add(resultSet.getString("phone"));
                    columnData.add(resultSet.getString("address"));
                    columnData.add(resultSet.getString("emailAddress"));
                }
                
                table.addRow(columnData);
            }
             
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Data.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Data.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }
    
